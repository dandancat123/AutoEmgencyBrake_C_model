/*                     取输出较大值函数(float)                      */
/*---------------------------------------------------------------*/

float MAX(float a, float b){
    return (a>b) ? a : b;
}

/*---------------------------------------------------------------*/
/*                     取输出较大值函数(int)                        */
/*---------------------------------------------------------------*/

int MAX_int(int a, int b){
    return (a>b) ? a : b;
}
